// Array.from() crea una nueva instancia de Array a partir de un valor iterable.
let arr_from = Array.from('123456789');
console.log(arr_from); 

/*
Array(9) [ "1", "2", "3", "4", "5", "6", "7", "8", "9" ];
*/


let arr = Array.from("Maria Otalvaro");
